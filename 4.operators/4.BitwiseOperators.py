m = 5  # Binary: 0101
n = 3  # Binary: 0011

# Bitwise AND
print(m & n)  # Output: 1 (Binary: 0001)

# Bitwise OR
print(m | n)  # Output: 7 (Binary: 0111)

# Bitwise XOR
print(m ^ n)  # Output: 6 (Binary: 0110)

# Bitwise NOT
print(~m)  # Output: -6 (Binary: 1010)

# Bitwise left shift
print(m << 1)  # Output: 10 (Binary: 1010)

# Bitwise right shift
print(m >> 1)  # Output: 2 (Binary: 0010)
