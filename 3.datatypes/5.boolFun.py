# False is already a boolean value, and it remains False
print(bool(False))  # Output: False

# None is considered as False in a boolean context
print(bool(None))  # Output: False

# The integer 0 is considered as False in a boolean context
print(bool(0))  # Output: False

# An empty string is considered as False in a boolean context
print(bool(""))  # Output: False

# An empty tuple is considered as False in a boolean context
print(bool(()))  # Output: False

# An empty list is considered as False in a boolean context
print(bool([]))  # Output: False

# An empty dictionary is considered as False in a boolean context
print(bool({}))  # Output: False
