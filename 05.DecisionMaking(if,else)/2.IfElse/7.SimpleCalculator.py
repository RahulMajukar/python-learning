a = 10
b = 5
operation = "subtract"

if operation == "add":
    print(f"The result is: {a + b}")
elif operation == "subtract":
    print(f"The result is: {a - b}")
elif operation == "multiply":
    print(f"The result is: {a * b}")
elif operation == "divide":
    print(f"The result is: {a / b}")
else:
    print("Invalid operation")
